            <br><br>Уже в Древней Греции античные мыслители задались вопросом
            о фундаментальной структуре материи. По научной моде тех лет греки
            искали первоэлементы. В результате этих поисков у греков появилось
            несколько совершенно различных наборов первоэлементов и даже
            концепция атомов в качестве экстравагантного довеска. Но греки не могли
            сделать выбор между разными наборами, поскольку для выбора было
            недостаточно только логических аргументов, а до идеи постановки
            решающего эксперимента оставалось почти 2000 лет.
            Только на рубеже XVII–XVIII веков физика сформировалась как наука,
            основной движущей силой которой является эксперимент, и оставалась
            ею вплоть до первой четверти XX века. Именно неожиданные
            экспериментальные результаты послужили толчком к появлению

            <br><br>классической электродинамики, специальной теории относительности
            и квантовой механики.
            Однако в 1928 году все изменилось. Выдающийся английский физик-
            теоретик, один из создателей квантовой механики Поль Дирак написал
            релятивистское квантовое уравнение для частиц с полуцелым спином. Это
            уравнение обладало одной важной особенностью, которую Дирак в него
            не закладывал: если у этого уравнения существовало решение для частиц
            с отрицательным электрическим зарядом, то неминуемо появлялось
            дополнительное решение для частиц с положительным зарядом. В начале
            1930-х годов была известна всего одна частица с полуцелым спином
            и отрицательным зарядом — это был электрон — и одна частица с полуцелым
            спином и положительным зарядом, и это был протон. Поначалу физики
            думали, что два решения уравнения Дирака соответствуют именно этим двум
            частицам. Но очень скоро немецкий математик Герман Вейль доказал, что
            частицы из уравнения Дирака с положительными и отрицательными
            зарядами должны иметь одинаковые массы. И тут возникла проблема,
            поскольку протон примерно в 2000 раз тяжелее электрона.
            То есть теория Дирака предсказывала принципиально новый факт. Говоря
            современным языком, Поль Дирак предсказал античастицы. Только поначалу
            в них никто не поверил, а самого Дирака критиковали за якобы ошибочное
            уравнение. И зря. Ведь уже год, как античастицы были открыты. Только
            об этом не догадывался даже их первооткрыватель — талантливый советский
            физик-экспериментатор Дмитрий Владимирович Скобельцын. Дело в том,
            что он был увлечен актуальной для того времени проблемой: изучением
            состава космических лучей, то есть частиц, которые попадают на Землю
            из космоса. Чтобы измерить импульс частиц космических лучей и их заряд,
            Скобельцын поместил камеру Вильсона — новейший для 1930-х годов
            прибор, который регистрировал треки заряженных частиц, — в постоянное
            магнитное поле. В такой камере пришедшие из космоса положительно
            заряженные частицы должны закручиваться в одну сторону,
            а отрицательные — в другую. Скобельцын наблюдал несколько треков,
            похожих на треки электронов, но закручивающихся в противоположную
            сторону. С высоты современного знания мы понимаем, что такие треки
            оставляли позитроны. Но ученый предположил, что эти треки оставляют
            электроны, которые летят с поверхности Земли, где образуются в результате
            естественной радиоактивности, и перестал интересоваться данными
            треками.
            Поэтому первооткрывателем позитронов во всем мире считается Карл
            Андерсон. Этот блестящий американский экспериментатор знал о теории

            <br><br>Дирака и захотел экспериментально проверить существование «электронов
            с другим зарядом». Андерсон использовал методику Скобельцына
            с небольшим дополнением, которое сделало американского
            экспериментатора Нобелевским лауреатом: он поместил в камеру Вильсона
            свинцовую пластину. Заряженная частица, попадая в пластину, теряет часть
            своей энергии, ее импульс уменьшается, кривизна трека в магнитном поле
            меняется. Поэтому по изменению кривизны трека можно понять, с какой
            стороны от свинцовой пластины в камеру влетела частица. Это была
            та информация, которой не имел Скобельцын, чтобы открыть позитрон.
            Оказалось, что частицы, треки которых похожи на треки электронов,
            но закручены в другую сторону, летят из космоса так же, как и обычные
            электроны. Андерсон поставил свой эксперимент в 1932 году. Этот год
            считается годом открытия античастиц и годом, начиная с которого теория
            в физике частиц начала опережать эксперимент. Нейтрино, бозон Хиггса,
            топ-кварк сначала были предсказаны теоретиками. Порой эксперименты
            подтверждали теорию спустя полвека, как это было, например, с бозоном
            Хиггса.
            Можно сказать, что на новом уровне мы вернулись к ситуации, которая была
            в Древней Греции: теоретики предлагают множество новых
            фундаментальных концепций, как когда-то греки предлагали различные
            наборы первоэлементов. Только теперь экспериментаторы пытаются
            проверять эти концепции, если есть подобная технологическая возможность.
            А что с антипротоном? Это вторая античастица, которая была открыта
            физиками. Ее обнаружила в 1955 году на протонном ускорителе группа
            талантливого итальянского физика Эмилио Сегре, бежавшего от нацистов
            в Америку. Открытие было удостоено Нобелевской премии за 1959 год. Почти
            одновременно с антипротоном был открыт антинейтрон.
            В настоящее время обнаружены сотни античастиц. Любая заряженная
            частица, необязательно с полуцелым спином, имеет свою античастицу.
            За открытие античастиц больше не вручают Нобелевские премии.
            А обнаруженное еще Андерсоном свойство частицы и античастицы при
            взаимодействии превращаться в фотоны — аннигилировать — породило
            одну из фундаментальных загадок современной физики — барионную
            асимметрию Вселенной. Уравнение Дирака давно признано всеми физиками
            и легло в основание квантовой теории поля.
            <br><br><span style="font-weight: 600;">От античастиц к антиматерии</span>

            <br><br>Eсли еще в 1960-е годы физики могли получать позитроны, антипротоны
            и антинейтроны, то, казалось бы, отсюда один шаг до синтеза антиматерии,
            например антиводорода. Однако на этом пути встают большие трудности.
            Чтобы создать атомы и молекулы антиматерии, мало получить их составные
            кирпичики — античастицы. Эти античастицы нужно замедлить. Но, главное,
            антиматерию нужно хранить в мире, который состоит из материи.
            Античастицы нельзя просто положить в коробку: они проаннигилируют
            со стенками коробки. Если мы хотим сохранить античастицы, то надо хранить
            их в вакууме и в «сосуде без стенок». Для заряженных частиц в качестве
            подобного сосуда может использоваться сильное неоднородное магнитное
            поле. Задача удержания нейтральных частиц куда более сложная,
            но со временем она тоже была решена при помощи магнитного поля.
            В настоящее время антиводород удерживается в магнитных ловушках
            Пеннинга почти 20 минут.
            Синтез антиматерии логично начать с синтеза антиядер. Однако
            на сегодняшний день в этом направлении не удалось достигнуть большого
            прогресса. Синтезированы лишь антигелий-3, который состоит из двух
            антипротонов и одного антинейтрона, и антигелий-4, в состав которого
            входят два антипротона и два антинейтрона. (Заметим, что антигелий-3 был
            синтезирован в подмосковном Институте физики высоких энергий
            на ускорителе У-70, который в настоящее время является самым
            высокоэнергичным ускорителем частиц на территории России.)
            Еще меньший прогресс достигнут при синтезе антиатомов. В настоящее
            время синтезированы только атомы антиводорода. Единичные атомы
            антиводорода были синтезированы в Европейском центре физики частиц
            (ЦЕРН) только в 1995 году. Настоящий прорыв произошел в 2002 году, когда
            было синтезировано около 50 миллионов атомов антиводорода. С тех пор
            ЦЕРН является мировым лидером в области изучения физических
            и химических свойств антиматерии.
            <br><br><span style="font-weight: 600;">Античастицы и фундаментальные законы природы</span>
            <br><br>В современной физике симметрии играют исключительную роль.
            В квантовой теории поля одной из самых важных симметрий является так
            называемая СРТ-симметрия, то есть симметрия относительно
            одновременной замены всех зарядов на противоположные ©, зеркального
            отражения пространства (Р) и обращения вспять хода времени (Т). Считается,
            что в природе могут реализовываться только СРТ-симметричные теории.
            Из СРТ-симметрии следует множество свойств, которым должны подчиняться

            <br><br>частицы и античастицы, — например, равенство масс обеих. В настоящее
            время интересно, как ведут себя не столько отдельные античастицы, сколько
            более сложные антиобъекты, такие как ядра и атомы. Например, в ЦЕРН
            активно исследуют спектроскопические свойства атомов антиводорода. СРТ-
            симметрия требует, чтобы эти свойства были точно такими же, как и у атома
            водорода. А еще атом антиводорода должен падать в гравитационном поле
            Земли точно так же, как атом водорода. И такой эксперимент сейчас
            проводится в ЦЕРН. Так что ЦЕРН — это не только Большой адронный
            коллайдер и бозон Хиггса. Это в том числе проверка фундаментальных
            симметрий природы. Для понимания окружающего мира эти симметрии даже
            важнее, чем бозон Хиггса. Пока эксперименты не смогли найти ни одного
            признака нарушения СРТ-симметрии.
            Теперь оглянемся вокруг и зададимся еще одним естественным вопросом:
            почему нас окружает только материя? И куда из нашего мира исчезла
            антиматерия? Эта проблема получила название барионной асимметрии
            Вселенной. Из СРТ-теоремы наивно ожидать, что после Большого взрыва
            материи и антиматерии было равное количество. А значит, рано или поздно
            может произойти глобальная аннигиляция. И по безжизненной Вселенной
            будут метаться лишь почти не взаимодействующие одинокие фотоны.
            Загадка барионной асимметрии пока не разгадана. Тут можно предложить
            несколько ответов. Например, из материи состоит наша Солнечная система,
            а другая звездная система, расположенная далеко от нашей, состоит
            из антиматерии. Но тогда непонятно, по каким причинам вместо
            аннигиляции материя и антиматерия предпочли разделиться
            в пространстве? Да и звездных антимиров астрономы не наблюдают.
            Еще одну идею в 1967 году предложил советский академик, лауреат
            Нобелевской премии мира Андрей Дмитриевич Сахаров. Он предположил,
            что барионное число — то самое, о котором мы говорили в начале этой
            статьи, — нарушается, и дополнительно привлек экспериментальный факт
            нарушения комбинированной зарядовой © и пространственной (Р) четности.
            Тогда нестабильные частицы могут распадаться несколько иначе, чем
            нестабильные античастицы. И этого оказывается достаточно, чтобы в конце
            концов материи стало немного больше, чем антиматерии. Остальная материя
            и антиматерия проаннигилировали. А из небольшого избытка материи
            состоят все объекты во Вселенной. В настоящее время теория Сахарова
            дополнена и развита. Но основная идея осталась неизменной.
            <br><br><span style="font-weight: 600;">На антиматерии к звездам</span>

            <br><br>Не будет преувеличением сказать, что человечество мечтает полететь
            к звездам. Но даже до ближайшей звезды, Проксимы Центавра, свет
            от Солнца идет более трех лет. До остальных звезд гораздо дальше. Фантасты
            легко преодолевают столь гигантские расстояния при помощи
            пространственно-временных туннелей, гипердвигателей, десятого
            измерения и прочих удобных, но, увы, всего лишь воображаемых способов
            передвижения. В реальном мире космолетам первых звездопроходцев
            придется двигаться в одном пространстве со светом и желательно
            со скоростью, близкой к скорости света. При этом мы хотим, чтобы такой
            космолет имел наименьшую возможную массу. В этой ситуации лучшего
            топлива, чем антиматерия, для космолета не найти. Действительно, вся
            масса топлива при аннигиляции переходит в фотоны, которые вылетают
            из сопла со скоростью света. Фотоны должны разогнать космолет до очень
            больших скоростей, которые составляют доли скорости света. А значит,
            полет до Проксимы Центавра может составить, скажем, тридцать лет. Это
            много, но звездопроходцы успеют вернуться на Землю в течение жизни
            одного поколения. Что дальше? Можно как в фантастике 1950–1960-х годов:
            космолетчики, почти нестареющие из-за парадокса близнецов, и девушки,
            которые ждут их на Земле в криогенных камерах. Космическая романтика
            золотых шестидесятых или суровые будни две тысячи пятидесятых? А ведь
            все началось с необычного уравнения Дирака, которое неизбежно должно
            было иметь два решения, и Карла Андерсона, который догадался вставить
            в камеру Вильсона свинцовую пластину.</p>
