            сильновзаимодействующие частицы все равно различаются барионным
            числом, которое равно +1 для нейтрона и -1 для антинейтрона.
            Бывают ли ситуации, когда и барионный, и электрический заряды равны
            нулю? Да, например, в случае с мезонами. Они состоят из кварка
            и антикварка, и по определению их барионный заряд — ноль. Рассмотрим,
            например, электрически нейтральные К-мезоны — удивительные частицы,
            в которых было открыто нарушение комбинированной пространственной
            и зарядовой четности. Существуют К0-мезон и анти-К0-мезон. Электрический
            и барионный заряды обеих частиц равны нулю. Почему же они тогда
            считаются частицей и античастицей? В данном случае различается
            кварковый состав мезонов. К0-мезон состоит из анти-s-кварка и d-кварка.
            Анти-К0-мезон состоит, наоборот, из s-кварка и анти-d-кварка. Странный
            кварк — s — обладает новым квантовым числом или зарядом — странностью.
            Странность различна для s- и анти-s-кварков точно так же, как барионный
            заряд различается для протонов и антипротонов; d-кварки и d-антикварки
            имеют свое квантовое число, аналогичное странности. Эти заряды

            <br><br>позволяют различать между собой электрически и барионно-нейтральные
            К0- и анти-К0-мезоны.
