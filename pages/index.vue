          <p class="all_theory_txt"><span style="font-weight: 600;">Физик Антон Баушев о поисках темной материи, из чего она может состоять и о том, какие теории уже отброшены</span>
            <br><br>Точно сказать, из чего состоит темная материя, мы пока не можем. Но мы можем сказать, из чего
            она точно не состоит. Например, ее главным компонентом точно не являются все те частицы,
            которые нам уже известны. Также она не может состоять из тех черных дыр, которые образуются
            в результате эволюции звезд. Но при этом существуют теории, согласно которым темная материя
            может состоять из маленьких (или даже не очень маленьких) черных дыр, образовавшихся в
            ранней Вселенной, однако сейчас все же более популярна точка зрения, что темная материя —
            это какие-то неизвестные науке элементарные частицы.

            <br><br>До недавнего времени (скажем, в 2012 году) самой популярной частицей такого рода был вимп
            (WIMP, Weakly Interacting Massive Particle) — гипотетическая массивная частица, вступающая в
            обычное слабое взаимодействие. Однако долгие безуспешные поиски вимпа привели к тому, что
            интервал параметров, в котором он мог бы существовать, не противореча тем или иным
            экспериментальным данным, существенно сузился. В частности, были экспериментально
            опровергнуты многие модели легких вимпов. Однако отбросить вимп мы пока не можем, еще
            есть области параметров, которые не закрыты, где вимп вполне может существовать. Позволю
            себе сделать осторожный прогноз: эти области относительно легкодоступны, и в ближайшие
            10–20 лет они либо будут окончательно закрыты наблюдениями, либо вимп все-таки будет
            открыт.

            <br><br>Неудачи с поиском вимпа привели к тому, что научное сообщество сейчас находится в некотором
            смятении, поэтому предпочтения довольно субъективны: кому-то нравится идея, что темная
            материя состоит из аксионов, кому-то — что из стерильных нейтрино, кому-то нравится что-то
            еще. На деле это может оказаться что-то совсем нам неведомое. Разговор о значимых кандидатах
            можно вести на основании каких-то фактов, полученных на основе наблюдений, а таких фактов
            нет. Есть много теорий, которые одинаково хорошо описывают темную материю, но все они очень
            различны, и аргументированно выбрать самую оптимальную из них в настоящее время нельзя. В
            итоге может получиться так, что кто-то угадает ответ, а кому-то не повезет.

            <br><br>Например, в конце 70-х годов, когда темную материю только открыли, и немного позже, в начале
            1980-х, полагали, что она состоит из обычных нейтрино. Дело в том, что, помимо реликтового
            излучения, состоящего из фотонов, должно быть и реликтовое излучение, состоящее из нейтрино.
            Важное отличие состоит в том, что, в отличие от фотонов, нейтрино имеют массу. Поэтому энергия
            фотона может уменьшаться вплоть до нуля, и он всегда движется со скоростью света. Энергия
            нейтрино может уменьшаться только до массы покоя, а вот скорость может падать, и сейчас
            реликтовые нейтрино движутся сравнительно медленно, а поэтому, на первый взгляд, могли бы
            составлять темную материю. Однако, во-первых, с начала 1980-х верхний предел на массу
            нейтрино заметно улучшился, и поэтому выяснилось, что суммарной массы реликтовых нейтрино
            не хватает для объяснения всей скрытой массы. А во-вторых, скорости реликтовых нейтрино к
            моменту формирования галактик, например, были гораздо меньше скорости света, но все-таки
            очень велики (такая темная материя называется горячей). Поэтому формирование структур во
            Вселенной, где скрытая масса состоит из реликтовых нейтрино, протекало бы совершенно не так,
            как в нашей, и противоречит астрономическим наблюдениям. Таким образом, темная материя не
            может быть горячей и, в частности, состоять из реликтовых нейтрино.

            <br><br>Эксперименты по поиску темной материи сейчас идут буквально непрерывно. Например,
            установки прямого детектирования выключают только на модернизацию и техобслуживание, а в
            остальное время они работают и набирают статистику. Поиски следов аннигиляции темной
            материи тоже проходят постоянно, причем эти наблюдения, как правило, собирают информацию
            не только о темной материи, но и о других астрономических объектах. Например, в космосе
            летает гамма-телескоп и регистрирует все фотоны, которые ему встречаются. Его результаты
            можно использовать для изучения астрофизических источников космического излучения:
            пульсаров, квазаров… Но можно и искать в этих данных следы аннигиляции темной материи, и
            этим занимаются многие группы и в России, и в других странах. Но, к сожалению, пока никто
            ничего убедительного не нашел.
