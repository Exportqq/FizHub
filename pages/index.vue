            мезон, который состоит из анти-s-кварка и s-кварка, а его античастица,
            наоборот, из s-кварка и анти-s-кварка. Получается, что φ-мезон сам себе
            античастица. На самом деле частиц, подобных φ-мезону, существует
            множество. Самая известная из них, наверное, J/ψ — мезон, который состоит
            из очаровательного кварка и антикварка. Фотоны также тождественны себе.
            И переносчики слабого взаимодействия — Z0-бозоны — тоже. Но есть одна
            элементарная частица, для которой ответ на вопрос, тождественна ли она
            сама себе, не прояснен до сих пор. Эта частица — нейтрино. Она участвует
            только в слабом и гравитационном взаимодействиях. Впрочем,
            гравитационное взаимодействие при масштабах энергий, доступных
            в настоящее время, не играет никакой роли. Поэтому можно сказать, что
            нейтрино участвует только в слабых взаимодействиях. В квантовой теории
            поля для описания состояний нейтрино существуют два подхода. Первый —
            это так называемый дираковский подход, в рамках которого нейтрино
            и антинейтрино считаются нетождественными друг другу. Иными словами,
            с точки зрения теоретиков, нейтрино и антинейтрино похожи на электрон
            и позитрон. Второй — подход Майораны, в котором нейтрино
            и антинейтрино считаются тождественными друг другу. Выбор в пользу
            концепции Майораны может дать экспериментальное наблюдение двойного
            безнейтринного бета-распада ядер. Данный распад — один из самых сложных
            для экспериментального наблюдения. В настоящее время этот процесс все
