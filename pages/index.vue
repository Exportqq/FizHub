            каждое из которых также обладает своими зарядами. Скажем, протон,
            имеющий положительный электрический заряд, и антипротон, имеющий
            отрицательный электрический заряд, в сильном взаимодействии
            приобретают барионный заряд (или барионное число), равный +1 для
            протона и -1 для антипротона. Поэтому в случае, если электрический заряд
